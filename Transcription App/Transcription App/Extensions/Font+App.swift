
import SwiftUI

// MARK: - DM Sans Font Extension
extension Font {

    // Thin
    static func dmSansThin(size: CGFloat) -> Font {
        .custom("DMSans-Thin", size: size)
    }

    // ExtraLight
    static func dmSansExtraLight(size: CGFloat) -> Font {
        .custom("DMSans-ExtraLight", size: size)
    }

    // Light
    static func dmSansLight(size: CGFloat) -> Font {
        .custom("DMSans-Light", size: size)
    }

    // Regular
    static func dmSansRegular(size: CGFloat) -> Font {
        .custom("DMSans-Regular", size: size)
    }

    // Medium
    static func dmSansMedium(size: CGFloat) -> Font {
        .custom("DMSans-Medium", size: size)
    }

    // SemiBold
    static func dmSansSemiBold(size: CGFloat) -> Font {
        .custom("DMSans-SemiBold", size: size)
    }

    // Bold
    static func dmSansBold(size: CGFloat) -> Font {
        .custom("DMSans-Bold", size: size)
    }

    // ExtraBold
    static func dmSansExtraBold(size: CGFloat) -> Font {
        .custom("DMSans-ExtraBold", size: size)
    }

    // Black
    static func dmSansBlack(size: CGFloat) -> Font {
        .custom("DMSans-Black", size: size)
    }
}
